package application;

public class Ride {
	private String start;
	private String destination;
	private String date;
	private String time;
	
	public Ride(String start, String destination, String date, String time) {
		this.start = start;
		this.destination = destination;
		this.date = date;
		this.time = time;
	}
	
	public String getStart() {
		return start;
	}
	
	public String getDestination() {
		return destination;
	}
	
	public String getDate() {
		return date;
	}
	
	public String getTime() {
		return time;
	}
}
